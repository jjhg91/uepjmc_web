              <p  align="justify">La Unidad Educativa José María Carreño fue fundada en el año 1991, por los esposos Lic. Dora Lara de Venturelli y el Lic. Memory Venturelli, ubicada en la calle con el mismo nombre e identificada con el No.49
Esta Institución comenzó funcionas con siete (7) aulas de clases, en las cuales impartían los tres primeros niveles de preescolar hasta el sexto grado, desde su fundación septiembre del año 1.991 hasta julio del año 1.995.
.</p>
